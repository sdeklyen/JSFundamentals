//Assignment Operators

//Assignment Operator
let x = 5;

//Addition Assignment
let x = x + 1;
x += 1;

//Subtraction Assignment
x -= 1;

//Multiplication Assignment
x *= 1;

//Division Assignment
x /= 1;

//Remainder or Modulus Assignment
x = x % 1;
x %= 1;

//Exponential Assignment
x = x ** 1;
x **= 1;